    public void storeMinMaxRate(long cityId, Map<String, String> minRates, Map<String, String> maxRates) {
        long start = System.currentTimeMillis();
        DateFormat dateFormat = new SimpleDateFormat("MMMyy");
        String month = dateFormat.format(new Date());
        // Jedis jedis = new Jedis("172.16.19.186", 6379);
        Jedis jedis = new Jedis("127.0.0.1", 6379);
        final String minKey = HOTEL_DIRECTORY_MIN_RATE_CACHE_PREFIX + month + "_" + cityId;
        Map<String, String> minHash = jedis.hgetAll(minKey);
        //boolean setExpiry = false;
        if (minHash == null || minHash.size() == 0) {
            minHash = new HashMap<String, String>();
            //setExpiry = true;
        }

        for (String key : minRates.keySet()) {
            if (minHash.containsKey(key)) {
                double oldValue = Double.parseDouble(minHash.get(key));
                double newValue = Double.parseDouble(minRates.get(key));
                if (newValue < oldValue) {
                    minHash.put(key, String.valueOf(newValue));
                }
            } else {
                minHash.put(key, minRates.get(key));
            }
        }

        jedis.hmset(minKey, minHash);
        /*
         * if (setExpiry) { jedis.expireAt(minKey, getExpiryTime()); }
         */
        //setExpiry = false;
        final String maxKey = HOTEL_DIRECTORY_MAX_RATE_CACHE_PREFIX + month + "_" + cityId;
        Map<String, String> maxHash = jedis.hgetAll(maxKey);
        if (maxHash == null || maxHash.size() == 0) {
            maxHash = new HashMap<String, String>();
            //setExpiry = true;
        }
        for (String key : maxRates.keySet()) {
            if (maxHash.containsKey(key)) {
                double oldValue = Double.parseDouble(maxHash.get(key));
                double newValue = Double.parseDouble(maxRates.get(key));
                if (newValue > oldValue) {
                    maxHash.put(key, String.valueOf(newValue));
                }
            } else {
                maxHash.put(key, maxRates.get(key));
            }
        }

        jedis.hmset(maxKey, maxHash);
        /*
         * if (setExpiry) { jedis.expireAt(maxKey, getExpiryTime()); }
         */
        System.out.println("Execution time " + (System.currentTimeMillis() - start) + "ms");
    }

